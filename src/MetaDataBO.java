
public class MetaDataBO {
	private int gridSize;
	private int startXPoint;
	private int startYPoint;
	private int endXPoint;
	private int endYPoint;
	private int actualEndXPoint;
	private int actualEndYPoint;
	
	public MetaDataBO() {
		super();
	}

	public int getGridSize() {
		return gridSize;
	}

	public void setGridSize(int gridSize) {
		this.gridSize = gridSize;
	}

	public int getStartXPoint() {
		return startXPoint;
	}

	public void setStartXPoint(int startXPoint) {
		this.startXPoint = startXPoint;
	}

	public int getStartYPoint() {
		return startYPoint;
	}

	public void setStartYPoint(int startYPoint) {
		this.startYPoint = startYPoint;
	}

	public int getEndXPoint() {
		return endXPoint;
	}

	public void setEndXPoint(int endXPoint) {
		this.endXPoint = endXPoint;
	}

	public int getEndYPoint() {
		return endYPoint;
	}

	public void setEndYPoint(int endYPoint) {
		this.endYPoint = endYPoint;
	}

	public int getActualEndXPoint() {
		return actualEndXPoint;
	}

	public void setActualEndXPoint(int actualEndXPoint) {
		this.actualEndXPoint = actualEndXPoint;
	}

	public int getActualEndYPoint() {
		return actualEndYPoint;
	}

	public void setActualEndYPoint(int actualEndYPoint) {
		this.actualEndYPoint = actualEndYPoint;
	}	
}
